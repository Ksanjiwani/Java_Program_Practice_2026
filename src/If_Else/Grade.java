package If_Else;

public class Grade {
	    public static void main(String args[])
	    {
	    	int marks =72;
	    	if(marks > 90)
	    	{
	    		System.out.println("Distinction");
	    	}
	    	else if(marks > 75)
	    	{
	    		System.out.println("First class");
	    	}
	    	else if(marks > 50)
	    	{
	    		System.out.println("Second class");
	    	}
	    	else
	    	{
	    		System.out.println("Fail");
	    	}
	    }

}
