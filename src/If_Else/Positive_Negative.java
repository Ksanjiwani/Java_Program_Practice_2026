package If_Else;

public class Positive_Negative {
	public static void main(String args[])
	{
		int no = 0;
		if(no > 0)
		{
			System.out.println("The no is Positive");
		}
		else if(no == 0)
		{
			System.out.println("The no is Zero");
		}
		
		else
		{
			System.out.println("The no is Negative");
		}
	}

}
